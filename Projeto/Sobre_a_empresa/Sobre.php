                <p>A ligação da Frajola´s Pizzaria com as pizzas começou na década de 30, com o bisavô dos proprietários, o Sr. Antonio Napolitano, então pizzaiolo do Bar Caracu na Doze de Outubro. Nesta época, só existiam pizzas de mussarela e alicce, muitas das quais vendidas por imigrantes italianos em latas redondas pelas ruas de São Paulo.</p>
                

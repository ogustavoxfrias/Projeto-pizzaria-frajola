                <p>Com o pioneirismo veia a experiência e, em 1982, João Frajola´s e seu tio Alberto fundaram a primeira pizzaria do bairro: a Frajola´s Pizzaria na rua Coriolano. Esse pioneirismo foi reconhecido pela Administração Regional e o Museu da Lapa, que homenagearam a família pelos bons serviços prestados à comunidade. Em 1991 foi inaugurada mais uma casa, a da rua Pio XI, que nesse ano completa 21 anos e se localiza numa casa com uma historia curiosa, por abrigar festas na década de 50. Hoje, alguns clientes ainda matam saudades dos velhos tempos lá.</p>
                
